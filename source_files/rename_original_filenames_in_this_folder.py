def rena(dir=".",extensions=["xml","mscx","mscz","txt"]):
    os.chdir(dir)
    global dict
    ext = "|".join(extensions)
    for file in sorted(os.listdir(dir)):
        m = re.search(r'(.+)(\.('+ext+'))$',file)
        if m:
            name, extension = m.group(1), m.group(2)
            if name in dict:
                neu = dict[name]+extension
            else:
                print(f"{file} is not among the original names. Skipped.")
                continue
            os.rename(file,neu)
            print(f"{file} => {neu}")


